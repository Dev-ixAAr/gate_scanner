// App Router — GoRouter Configuration
//
// Defines all application routes with session-based access control.
//
// Route Guard Logic:
// - On app startup: reads session from secure storage
// - If NO session token found: redirect all routes to /setup
// - If session token EXISTS: redirect /setup to /home (already configured)
// - If session is REVOKED (401 from API): interceptor clears session and
//   navigates to /setup programmatically
//
// Phase 1: Placeholder screens — full implementation in Phase 2+
// ============================================================================

