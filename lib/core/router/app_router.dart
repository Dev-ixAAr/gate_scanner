/// Riverpod provider for the GoRouter instance.
///
/// Using @riverpod annotation generates a provider that can be refreshed
/// when session state changes, triggering route guard re-evaluation.
///
/// The [ref] parameter allows reading other providers (e.g., session state)
/// for use in the redirect guard.
@riverpod
GoRouter appRouter(AppRouterRef ref) {
  return GoRouter(
    // Default route when app starts.
    // The redirect function will override this based on session state.
    initialLocation: RouteNames.setup,

    // Enable debug logging for route transitions in development.
    // Set to false or use kDebugMode check before production release.
    debugLogDiagnostics: true,

    // --------------------------------------------------------------------------
    // Session-based route guard.
    //
    // Called on every navigation attempt (initial and subsequent).
    // Returns null to allow navigation, or a path string to redirect.
    //
    // Logic:
    // 1. Read session token from secure storage
    // 2. If no token: force redirect to /setup
    // 3. If token exists and navigating to /setup: redirect to /home
    //    (user is already configured — no need to show setup again)
    // 4. Otherwise: allow navigation to requested route
    // --------------------------------------------------------------------------
    redirect: (BuildContext context, GoRouterState state) async {
      // TODO: Replace with sessionServiceProvider in Phase 3
      // For Phase 1, read directly from secure storage for simplicity.
      // In Phase 2+, this will use the SessionService provider.
      final storage = ref.read(secureStorageServiceProvider);
      final sessionToken = await storage.read(StorageKeys.sessionToken);

      final bool hasSession = sessionToken != null && sessionToken.isNotEmpty;
      final bool isOnSetupRoute = state.matchedLocation == RouteNames.setup ||
          state.matchedLocation == RouteNames.setupScan;

      // No session: force user to setup screen regardless of requested route.
      if (!hasSession && !isOnSetupRoute) {
        return RouteNames.setup;
      }

      // Has session but trying to access setup: redirect to home.
      // Prevents a configured device from going back to setup accidentally.
      if (hasSession && isOnSetupRoute) {
        return RouteNames.home;
      }

      // Allow navigation to the requested route.
      return null;
    },

    // --------------------------------------------------------------------------
    // Route Definitions
    // --------------------------------------------------------------------------
    routes: [
      // Setup / Welcome Screen
      // Entry point for unconfigured devices.
      GoRoute(
        path: RouteNames.setup,
        name: 'setup',
        builder: (context, state) {
          // TODO: Replace with WelcomeSetupScreen() in Phase 4
          return const _PlaceholderScreen(
            title: 'Welcome Setup',
            subtitle: 'Scan your admin setup QR code to get started.',
            routePath: RouteNames.setup,
          );
        },
      ),

      // Setup QR Scan Screen
      // Camera view for scanning the admin setup QR code.
      GoRoute(
        path: RouteNames.setupScan,
        name: 'setup-scan',
        builder: (context, state) {
          // TODO: Replace with SetupQrScanScreen() in Phase 4
          return const _PlaceholderScreen(
            title: 'Scan Setup QR',
            subtitle: 'Point camera at the admin setup QR code.',
            routePath: RouteNames.setupScan,
          );
        },
      ),

      // Scanner Home Screen
      // Dashboard shown after successful event binding.
      GoRoute(
        path: RouteNames.home,
        name: 'home',
        builder: (context, state) {
          // TODO: Replace with ScannerHomeScreen() in Phase 6
          return const _PlaceholderScreen(
            title: 'Scanner Home',
            subtitle: 'Event dashboard. Session active.',
            routePath: RouteNames.home,
          );
        },
      ),

      // Ticket Scan Screen
      // Full-screen camera view for scanning attendee tickets.
      GoRoute(
        path: RouteNames.scan,
        name: 'scan',
        builder: (context, state) {
          // TODO: Replace with TicketScanScreen() in Phase 7
          return const _PlaceholderScreen(
            title: 'Ticket Scanner',
            subtitle: 'Point camera at attendee ticket QR code.',
            routePath: RouteNames.scan,
          );
        },
      ),

      // Manual Search Screen
      // Manual ticket reference entry for lookup.
      GoRoute(
        path: RouteNames.manualSearch,
        name: 'manual-search',
        builder: (context, state) {
          // TODO: Replace with ManualSearchScreen() in Phase 8
          return const _PlaceholderScreen(
            title: 'Manual Search',
            subtitle: 'Enter ticket reference to search manually.',
            routePath: RouteNames.manualSearch,
          );
        },
      ),

      // Session Settings Screen
      // Device info, session info, logout/reset actions.
      GoRoute(
        path: RouteNames.settings,
        name: 'settings',
        builder: (context, state) {
          // TODO: Replace with SessionSettingsScreen() in Phase 9
          return const _PlaceholderScreen(
            title: 'Settings',
            subtitle: 'Session and device settings.',
            routePath: RouteNames.settings,
          );
        },
      ),
    ],

    // --------------------------------------------------------------------------
    // Error handler for unknown routes.
    // Shows a simple error screen instead of crashing.
    // --------------------------------------------------------------------------
    errorBuilder: (context, state) {
      return Scaffold(
        backgroundColor: Colors.black,
        body: Center(
          child: Padding(
            padding: const EdgeInsets.all(24.0),
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                const Icon(
                  Icons.error_outline,
                  color: Colors.red,
                  size: 64,
                ),
                const SizedBox(height: 16),
                const Text(
                  'Page Not Found',
                  style: TextStyle(
                    color: Colors.white,
                    fontSize: 24,
                    fontWeight: FontWeight.bold,
                  ),
                ),
                const SizedBox(height: 8),
                Text(
                  'Route "${state.uri}" does not exist.',
                  style: const TextStyle(color: Colors.white60),
                  textAlign: TextAlign.center,
                ),
                const SizedBox(height: 24),
                ElevatedButton(
                  onPressed: () => context.go(RouteNames.setup),
                  child: const Text('Go to Setup'),
                ),
              ],
            ),
          ),
        ),
      );
    },
  );
}

